        <h4>“The Poor in early 19th Century Surrey” – Judy Hill</h4>
        <p>Judy Hill has previously spoken to us about the Swing Riots in Surrey. This time she will tell us about the early 19th century Poor Laws and medical care for the poor in Surrey.</p>
