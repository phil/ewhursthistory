        <h4>“A Drop of the Hard (and the Soft) Stuff” – David Rose</h4>
        <p>We welcome David Rose for a return visit. David will tell us about Guildford’s brewing and mineral water industries.</p>
      </li>

      <li>
        <p class="date">Tuesday 16th April 2013</p>
        <h4>“The Making of Guildford Blue” – Matthew Alexander</h4>
        <p>Former Curator of Guildford Museum, Matthew Alexander, will tell us about the importance of the woollen industry and wool trade in South-West Surrey, from its medieval origins through to Tudor times.</p>
      </li>

      <li>
