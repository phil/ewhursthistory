      <li>
        <p class="date">Tuesday 16th October 2012</p>
        <h4>“Warrior – The Amazing Story of a Real War Horse” - Brough Scott</h4>
        <p>Brough Scott will tell us about Warrior, the horse belonging to his grandfather, General Jack Seeley. Jack Seeley’s book ‘Warrior’, originally published in 1934, has been reissued with an Introduction by Brough. The book tells the story of Warrior, and how a combination of both the horse’s extraordinary character and some unbelievable twists of fate, helped him survive a war which claimed the lives of 8 million horses.</p>
      </li>

      <li>
        <p class="date">Tuesday 20th November 2012</p>
        <h4>“Funny Money” – John Theobold</h4>
        <p>The shortage of coins in the 17th century led to the introduction of trade tokens issued by shopkeepers and tradesmen to continue their business. Collector John Theobald will tell us about trade tokens used in Surrey.</p>
      </li>

      <li>
        <p class="date">Tuesday 11th December 2012</p>
        <p>NB this is the 2nd Tuesday, not the 3rd.</p>
        <h4>Members’ Evening</h4>
        <h4>“What the Navy left behind” – Alan Webber</h4>
        <p>Chatham is the world's most complete example of an Historic Dockyard from the age of sail, and is home to one of the largest concentrations of listed buildings and Scheduled Ancient Monuments in the country. After the closure of the dockyard in 1984, Alan was the Forward Planning Manger and he will tell us about the military legacy of this amazing site. </p>
        <p>Followed by mulled wine and mince pies.</p>
      </li>

      <li>
        <p class="date">Tuesday 15th January 2013</p>
        <h4>“The History of Jacob Sheep” – Virginia Lloyd</h4>
        <p>Local sheep breeder Virginia Lloyd will tell us about the origins and history of this rare breed.</p>
      </li>

      <li>
        <p class="date">Tuesday 19th February 2013</p>
        <h4>Quiz Night and Supper - Details to follow.</h4>
      </li>

