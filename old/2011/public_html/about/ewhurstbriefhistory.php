		<h2 id="VictorianAnd20C">Victorian Period &amp; 20th Century</h2>
		<p>The village remained small and isolated until the late 19th century, when improved communications 
		and a growing appreciation of the beauty of the wild Surrey countryside encouraged many wealthy 
		businessmen and artistic people to move into the area. They commissioned architects to design new 
		houses for them, or restored and enlarged the old timber frame houses.</p>
