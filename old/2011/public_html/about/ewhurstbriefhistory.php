		<p>A school had been established in the village in 1846 and by the twentieth century the village 
		was a thriving and self-contained community with many shops and businesses. Today the village still 
		has a school, 2 churches, shops &amp; Post Office, local pubs and a wide range of sports facilities and social clubs.</p>
