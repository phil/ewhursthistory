		<h2 id="TheSaxons">The Saxons</h2>
		<p>After the Romans left, the Weald was still largely uninhabited and wild. The Saxons settled in 
		the sheltered valleys to the north but looked to the Weald as a source of timber and for hunting 
		and grazing animals. At first they would have only journeyed into the forest in the summer months, 
		but gradually small settlements developed and eventually these would have come to be inhabited all 
		the year round. The people who lived in these isolated farmsteads would have still looked to their 
		parent villages to the north for their communal needs and it was much later before a village was 
		established at Ewhurst.</p>

		<h2 id="TheNormans">The Norman Conquest and after</h2>
		<p>The feudal system of manors had developed under the Saxons and, at the time of the conquest, 
		Ewhurst was part of the Royal Manor of Gumshall. By the medieval period Ewhurst was developing 
		into a separate community with its own church and became a parish in its own right in 1291.</p>
