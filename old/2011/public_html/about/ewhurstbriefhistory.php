		<h2 id="Pre-history">Pre-history</h2>
		<p>Ewhurst lies on the edge of the Weald which, 125 million years ago, was an area of shallow 
		lakes and marshes. The Wealden clay is rich in fossils and in 1983 a complete skeleton of a 
		dinosaur was discovered. This was found to be a previously unknown species of flesh-eating 
		dinosaur and was named <a href="http://www.nhm.ac.uk/darwincentre/live/presentations/presentation_baryonyx.html" rel="external">Baryonyx Walkerii</a>, 
		meaning 'heavy claw', in honour of its finder, William Walker. It is now on display at the 
		<a href="http://www.nhm.ac.uk/" rel="external">Natural History Museum</a>.</p>

		<h2 id="Earlyman">Early man</h2>
		<p>The stone age and bronze age peoples avoided the dense impenetrable forest of the Weald in 
		favour of the higher ground to the north and this area was on of the last parts of Britain to 
		become permanently settled. At the end of the Iron Age a hill fort was constructed on Holmbury Hill.</p>

		<h2 id="TheRomans">The Romans</h2>
		<p>The roman road of Stane Street passes through this part of Surrey and a branch road runs almost 
		the full length of the parish from Rowhook in the south to Horseblock Hollow in the north. The Romans 
		established a villa at Rapsley, which was excavated in the 1960s. The site is thought to have been 
		occupied from AD80 - AD330 and interesting discoveries include fragments of a glass goblet and an 
		unusual vase decorated with a 'Mural Crown'. A tile-kiln had already been discovered and excavated 
		in 1836 to the south west of the villa site</p>
