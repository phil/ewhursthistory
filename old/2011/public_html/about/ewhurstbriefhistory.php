		<h2 id="MedivalAndTudor">The Medieval and Tudor periods</h2>
		<p>The richness of the Weald's natural resources led it to become the industrial centre of Britain, 
		as both the iron and glass industries needed large amounts of timber for fuel. There is a site of a 
		bloomery iron works at Coneyhurst Gill and two glass making sites at Ellen's Green and Summersbury. 
		The wealth of the area can also be seen in the many fine timber framed houses dating from this period.</p>
