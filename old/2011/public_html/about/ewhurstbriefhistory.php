		<h2 id="c17c18c19">17th, 18th &amp; 19th Centuries</h2>
		<p>By the 17th century coal replaced timber as fuel and the industries moved north and the area declined. 
		The heavy clay and poor roads contributed to its isolation. Farming and woodcrafts formed the basis of 
		the local economy but there was much poverty and poaching and smuggling was rife.</p>
