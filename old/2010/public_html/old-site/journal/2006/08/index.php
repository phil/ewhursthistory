                              <p>A wish List</p>
                                                            <h2>Spotlight</h2>

<p>Firstly spotlight. Now really like spotlight, the promise of a completely OS-level disk search tool sounded great, it worked pretty good too, if you were happy to wait for it to finish searching your entire harddrive for &#8216;a&#8217;, then &#8216;ap&#8217;, then &#8216;app&#8217; etc. Hopefully Leopard will supercharge spotlight, then i might be able to ditch Quicksilver.</p>

<h2>The Finder</h2>

<p>Arrrr, ok, we all hate the finder, and have done for quite some time, we know this, Apple knows this. although Apple has yet to release details, we all know the finder is in for a make-over, but please Apple, whatever you do, don&#8217;t add bloody tabs to it. I really cannot think of a worst use of <span class="caps">UI, </span>than tabs for a file management tool. My reason, the most common operation the finder does is moving/copying files, which doesn&#8217;t lend itself to tabs. Instead, i would like to see improvements to the side-bar and context menus. But hey, who knows, we might get a 3D finder?</p>

<h2>Safari</h2>

<p>Safari gets the upgrade to Webkit v3, pretty much a given.</p>

<h2>UI</h2>

