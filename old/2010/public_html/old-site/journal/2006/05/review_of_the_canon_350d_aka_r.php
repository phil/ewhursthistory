			<div class="entry-more">
            			<p>I&#8217;ve had the 350D for about 2 weeks now, and its going ok. I mean, some of the pics are great, and some arn&#8217;t. Its certainly different to point and shoot camera, its alot easier to take badly setup pictures, but it&#8217;s also got all the extra features that is was missing the Ixus. My first impression is, this camera feels more like a traditional film camera, than a digital. As in, i could quite happily take indoor pictures with the Ixus, in low light, at <span class="caps">ISO</span> 100, and the pictures would just look fine, no blurring, even without a flash. With the 350D, it seems to want more light, or it uses a slower shutter speed, which is how most of my not so good images have been taken. But unlike the Ixus, you can really crank up the <span class="caps">ISO </span>speed, even at 1600 the images don&#8217;t look too noisy, so there are work arounds to achiveing great pictures.</p>


