				<p>The First Digital Camera I bought was the [then] tiny Canan Ixus500, it was a great little camera. It coul do both point and shoot, and it had some more <em>advanced</em> features like long exposure, <span class="caps">ISO </span>speed etc. But the time soon came when i had outgrown it. Dont get me wrong, it was and still is a great camera.</p>

