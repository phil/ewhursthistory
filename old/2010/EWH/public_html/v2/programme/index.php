                Alan Crocker The Surrey Industrial History Awards<br />
                In 1983 the Surrey Industrial History Group decided to make an annual award, in the form of a commemorative plaque, to acknowledge the contribution made by an individual or organisation in conserving the industrial heritage of the county. Alan Crocker will tell us about the projects for which plaques have been awarded, including Baynards Railway Station, The Old Farm at Shere and Albury Pigeon House.<br />
              </p>
