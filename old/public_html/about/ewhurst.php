		<p>Ewhurst is a village about 35 miles south west of London on the Surrey Sussex border. 
		Close by is the larger village of Cranleigh and the nearest towns of Guildford, Dorking 
		and Horsham, are each about 10 miles away.</p>

		<p>The village takes its name from the Old English 'hyrst', meaning 'wooded-hill' and 'iw' 
		meaning 'yew tree' (the English Place Name Society suggests 'Yew Wood'). In the Natural 
		History and Antiquities of the County of Surrey published in 1719 Aubrey notes "the vast 
		quantities of yew trees that formerly abounded here". The first recorded spelling is 
		"Iuherst" in 1179. A popular interpretation is that the name derived from ewe-sheep as 
		the wool trade was important in medieval times, but this seems unlikely as the name is 
		much older and dates from the Saxon period. The village sign on the green, opposite the 
		Bulls Head, shows both a ewe-sheep and a yew tree and also incorporates the Royal Insignia 
		and the date 1953, as it was erected to commemorate the Coronation of Queen Elizabeth II.</p>

		<p>The parish of Ewhurst, like many in the southern part of Surrey, is long and thin, 
		stretching some six miles from Pitch Hill in the north to the hamlet of Ellen's Green on 
		the Sussex border in the south, but is less than two miles from east to west. It covers an 
		area of approximately 5,400 acres with a population at present of about 2,500. Above the 
		village Pitch Hill stands out from the Greensand escarpment with commanding views over the 
		Weald to the South Downs. Extending northwards is the Hurtwood, a large area of woodland and 
		common land forming part of the Surrey Hills Area of Outstanding Natural Beauty. Much of 
		the parish, however, is on the Weald Clay, which in ancient times was a vast forest and is, 
		today, despite the Great Storm of 1987, still one of the most densely wooded areas of Britain.</p>
