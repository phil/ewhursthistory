		<h2 id="Manorial">Manorial Records</h2>
		<p>The parish of Ewhurst is covered by seven manors - Gumshall Towerhill, Gumshall Netley, Somersbury, 
		Coneyhurst, Pollingfold, Vachery, and Westland.</p>
		<p>Manors developed as a feudal system of land holding during the Saxon period. The principal manor in this 
		area was the Manor of Gumesele which originally included the present day Gomshall, Shere, Ewhurst and Cranleigh, 
		but by the time of the Norman Conquest had been split into two - Shere (including Cranleigh) and Gumesele 
		(including Ewhurst). Shere was held by Edith, the widow of Edward the Confessor, but Gumsesle was a Royal 
		Manor held by King Harold himself. In 1154, Henry II divided the Manor of Gumesele into three - West Gomshall, 
		East Gomshall and Somersbury. Somersbury was wholly within what is now Ewhurst parish and both of the Manors 
		of Gomshall held parcels of land in Ewhurst. In 1240 West Gomshall was granted to the Abbey of Netley in 
		Hampshire and became known as Gomshall Netley. East Gomshall was granted to The Abbey of St Mary Graces, 
		Tower Hill, London in 1376 and became known as Gomshall Towerhill. After the dissolution both Manors were 
		acquired by the Bray family, who already held Shere Eboracum (Shere) and Shere Vachery (Cranleigh). The Bray 
		family still hold the title of Lord of the Manor.</p>
		<p>Smaller manors were created by a process known as subinfeudation. Pollingfold was a sub-manor held from 
		Gomshall Towerhill. and Coneyhurst was a sub-manor held from Somersbury. Breach and Maybanks are both described 
		as manors in the Victoria County History, but no records survive for them. Breach was held from Gomshall Netley 
		and Maybanks from Pollingfold. In addition parts of the western side of the parish were included in the manor 
		of Shere Vachery and the Manor of Westland which was part of the Evelyn Estates held land in Ellen's Green.</p>
