		<p>Following years of painstaking research by the author, Janet Balchin, Ewhurst History Society is 
		publishing a book "Ewhurst Houses and People".  The format of the book follows the Society's highly 
		successful &ldquo;Ewhurst and Ellens Green&rdquo;, with 144 A4 landscape pages and is illustrated with nearly 
		two hundred illustrations, which include old photographs and postcards, engravings, portraits, 
		paintings and line drawings. The cover shows a watercolour of the unusual 1930's house Marylands.</p>

		<div class="ImageFrame">
			<img src="mount.jpg" class="ImageStd" alt="The Mount 1927">
			<p>The Mount, 1927</p>
		</div>

		<p>The book traces the history of over 130 historic houses. For each there is a description of the 
		architecture, followed by stories of the owners and their families who lived there.  The development 
		of timber framed houses is traced from careful examination of the structure – the author has spent 
		many hours climbing through dusty attics – while in contrast the large Victorian and Edwardian 
		houses are well documented, many having been designed by well known architects including Philip 
		Webb, George &amp; Peto, Ernest Newton, Oliver Hill and father and son Sir Aston and Maurice Webb.  
		The family history has been compiled from census records and documents going back to medieval 
		times: One house was owned by the same family for 700 years.  Ewhurst houses have been used for 
		film and television productions, provided wartime refuge for London offices, restyled as Swiss 
		chalets, and have been schools, pubs, shops, guesthouses and workshops.</p>

		<div class="ImageFrame">
