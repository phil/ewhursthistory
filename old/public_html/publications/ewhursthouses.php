		<p>The parish of Ewhurst has a wealth of interesting houses, from medieval timber framed cottages 
		to large Victorian 'Arts &amp; Crafts' style houses built on the slopes of Pitch Hill.  These have 
		attracted many interesting owners, including businessmen, artists, scientists and inventors.</p>

		<div class="ImageFrame">
			<img src="woolpits.jpg" class="ImageStd" alt="Woolpits, The Duke of Kent School">
			<p>Architects Drawing of Woolpits.</p>
